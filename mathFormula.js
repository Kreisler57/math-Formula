function cariVolumeKubus(panjang,lebar,tinggi){
    return ( panjang * lebar * tinggi);
};

//Q2:

function cariKelilingSegitiga(sisi1,sisi2,sisi3){
    return (sisi1 + sisi2 + sisi3);
};

//Q3:

function cariLuasSegitiga (alas, tinggi){
    return (1/2 * alas * tinggi);
};

//Q4:

function cariVolumePrisma (panjang, lebar, tinggi){
    return (((panjang * lebar) / 2) * tinggi);
};

